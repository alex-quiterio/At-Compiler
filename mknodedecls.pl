#!/usr/bin/perl
#

$decls = $incls = '';

open NODES, "find nodes -name \\*.h -print |"
  or die "Could not list node headers";
while (<NODES>) {
  if (m/\ball\.h\b/) { next; }
  chomp;
  $decl = $incl = $_;
  #
  # Handle declaration
  #
  $i = $decl =~ s-(([[:alnum:]]|_)+)/-namespace $1 { -g;
  $decl =~ s-(([[:alnum:]]|_)+?)\.h-class $1; -;
  $decls .= $decl;
  for ($j = 0; $j < $i; $j++) {
    $decls .= "} ";
  }
  $decls .= "\n";
  #
  # Handle include
  #
  $incl =~ s/^/#include "/;
  $incl =~ s/$/"/;
  $incls .= "$incl\n";
}
close NODES;

# "nodes" (dir) in $decls should be "node" (namespace)
$decls =~ s/\bnodes\b/node/g;
chomp $decls;

###########################################################################
###########################################################################
###########################################################################
